public class Variable { 
	public static void main(String[] args) {
		String name = "John";  //String collect as message or word 
		System.out.println(name);
		int myNum = 15;		//int collect as Intreger 
		System.out.println(myNum);
		char myChar = 'F'; 	//char collect as single or character
		System.out.println(myChar); 
		float myFloat = 5.99F;  //float collect as decimal if don't f means collect with double  
		System.out.println(myFloat);
		boolean myBoolean = true; //collect as boolean 
		System.out.println(myBoolean);  
	}
}
